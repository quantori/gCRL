edges   <- which(a_matrix != 0, arr.ind = TRUE)
edge_df <- data.frame(
  from   = row_labels[edges[, 1]],
  to     = col_labels[edges[, 2]],
  weight = a_matrix[edges])

g <- graph_from_data_frame(edge_df, directed = FALSE,
                            vertices = c(row_labels, col_labels))
V(g)$type  <- c(rep(0, num_factors), rep(1, num_factors))
V(g)$color <- c(grey_colors[seq_len(num_factors)],    # left: Z_hat — greys
                factor_colors[seq_len(num_factors)])  # right: Z    — community colours
V(g)$shape <- 'square'
V(g)$size  <- 30

y_pos         <- seq(1, 0, length.out = num_factors)
layout_manual <- rbind(cbind(x = -1, y = y_pos),
                        cbind(x =  1, y = y_pos))
rownames(layout_manual) <- c(row_labels, col_labels)

wts      <- E(g)$weight
wts_norm <- (abs(wts) - min(abs(wts))) / (max(abs(wts)) - min(abs(wts)) + 1e-9)
E(g)$width <- 1 + 4 * wts_norm
E(g)$color <- ifelse(wts > 0,
                     rgb(1, 0.1, 0.1, alpha = wts_norm),
                     rgb(0.1, 0.1, 1,  alpha = wts_norm))

plot_a_matrix <- function() {
  sz <- 4 + num_factors / 5
  igraph::plot.igraph(g, layout = layout_manual[V(g)$name, ],
                      vertex.label = NA,
                      edge.width   = E(g)$width,
                      edge.color   = E(g)$color,
                      margin = 0.2)
  coords      <- layout_manual[V(g)$name, ]
  coords[, 2] <- (2 * (coords[, 2] - min(coords[, 2])) /
                    (max(coords[, 2]) - min(coords[, 2]))) - 1
  offset_x <- 0.4
  for (i in seq_along(V(g))) {
    nm <- V(g)$name[i]
    x  <- coords[i, 1]
    y  <- coords[i, 2]
    if (V(g)$type[i] == 0) {
      k   <- sub('hat_Z_', '', nm, fixed = TRUE)   # "1".."7"
      lbl <- TeX(paste0('$\\hat{Z}_{', k, '}$'))
      text(x - offset_x, y, labels = lbl)
    } else {
      if (nm == 'Z_Global') {
        lbl <- TeX('$Z_{Global}$')
      } else {
        k   <- sub('Z_', '', nm, fixed = TRUE)
        lbl <- TeX(paste0('$Z_{', k, '}$'))
      }
      text(x + offset_x, y, labels = lbl)
    }
  }
}

