# ── T-Mex heatmap helper ───────────────────────────────────────
plot_tmex_heatmap <- function(mat, row_ann, col_ann, row_titles, col_titles,
                               tmex_val, out_path) {
  row_max_idx <- apply(abs(mat), 1, which.max)
  ht <- Heatmap(mat,
                row_split    = seq_len(nrow(mat)),
                row_title    = row_titles,
                column_split = seq_len(ncol(mat)),
                column_title = col_titles,
                cluster_rows = FALSE, cluster_columns = FALSE,
                show_row_names = FALSE, show_column_names = FALSE,
                left_annotation = row_ann,
                top_annotation  = col_ann,
                show_heatmap_legend = FALSE,
                cell_fun = function(j, i, x, y, w, h, fill) {
                  if (j == row_max_idx[i])
                    grid.text(sprintf('%.3f', mat[i, j]), x, y,
                              gp = gpar(fontsize = 10, fontface = 'bold'))
                })
  sz <- 4 + max(nrow(mat), ncol(mat)) / 5
  svg(out_path, height = sz, width = sz)
  draw(ht, column_title    = paste0('T-Mex = ', round(tmex_val, 3)),
       column_title_side   = 'bottom',
       column_title_gp     = gpar(fontsize = 20))
  dev.off()
}

