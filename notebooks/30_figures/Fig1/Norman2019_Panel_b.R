# ── A-matrix bipartite graph ───────────────────────────────────
# Rows (left)  = Z_hat factors  — coloured by community / grey for global
# Cols (right) = Z latent dims  — same colour scheme
row_labels <- paste0('hat_Z_', seq_len(num_factors))   # hat_Z_1 .. hat_Z_7
