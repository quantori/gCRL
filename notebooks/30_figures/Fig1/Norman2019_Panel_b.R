make_col_ann <- function(colors_vec = factor_colors,
                          labels_vec = factor_labels,
                          which_side = 'col') {
  cc <- colors_vec; names(cc) <- labels_vec
  if (which_side == 'col')
    HeatmapAnnotation(CF = labels_vec, col = list(CF = cc),
                      show_annotation_name = FALSE, show_legend = FALSE)
  else
    HeatmapAnnotation(CF = labels_vec, col = list(CF = cc),
                      show_annotation_name = FALSE, show_legend = FALSE, which = 'row')
}

